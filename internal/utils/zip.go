package utils

import (
	"archive/zip"
	"bytes"
	"io"
	"net/http"
)

func ReadZipFile(zf *zip.File) ([]byte, error) {
	f, err := zf.Open()
	if err != nil {
		return nil, err
	}
	defer f.Close()
	return io.ReadAll(f)
}

func ZipReaderFromResponse(resp *http.Response) (*zip.Reader, error) {
	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}

	return zip.NewReader(bytes.NewReader(body), int64(len(body)))
}
